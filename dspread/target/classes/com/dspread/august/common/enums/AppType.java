package com.dspread.august.common.enums;

public enum AppType {

    app_sharing(1,"值享"),
    app_creator(2, "创客");

    private int value;
    private String text;

    AppType(int value, String text) {
        this.value = value;
        this.text = text;
    }

    public int getValue() {
        return value;
    }

    public String getText() {
        return text;
    }
}
